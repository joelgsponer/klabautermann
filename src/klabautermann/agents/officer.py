class OfficerOfTheWatch(BaseAgent):
    """
    Proactive alert and monitoring agent.

    The Officer monitors conditions that warrant proactive notification -
    approaching deadlines, upcoming meetings, schedule conflicts, and
    unusual patterns. Respects "quiet mode" during focused work periods.
    """

    def __init__(
        self,
        neo4j_client: Neo4jClient,
        config: OfficerConfig | None = None,
        channel_manager: ChannelManager | None = None,
    ) -> None:
        """
        Initialize OfficerOfTheWatch.

        Args:
            neo4j_client: Connected Neo4j client for graph operations.
            config: Optional configuration for alert behavior.
            channel_manager: Optional channel manager for sending alerts.
                            If provided, enables send_alerts_to_channels().
        """
        super().__init__(name="officer_of_the_watch")
        self.neo4j = neo4j_client
        self.officer_config = config or OfficerConfig()
        self._channel_manager = channel_manager

        # Track recently sent alerts for debouncing
        self._recent_alerts: dict[str, datetime] = {}

        # Current quiet mode state
        self._quiet_mode = False
        self._current_deep_work_event: str | None = None

    async def process_message(self, msg: AgentMessage) -> AgentMessage | None:
        """
        Process an incoming message.

        OfficerOfTheWatch responds to monitoring and alert commands.

        Args:
            msg: The incoming agent message.

        Returns:
            Response message with alert results.
        """
        trace_id = msg.trace_id
        payload = msg.payload or {}

        operation = payload.get("operation", "check_conditions")

        logger.info(
            f"[CHART] OfficerOfTheWatch processing {operation}",
            extra={"trace_id": trace_id, "agent_name": self.name},
        )

        if operation == "check_conditions":
            result = await self.check_conditions(trace_id=trace_id)
            result_payload = result.to_dict()
        elif operation == "check_deadlines":
            alerts = await self.check_upcoming_deadlines(trace_id=trace_id)
            result_payload = {
                "alerts": [a.to_dict() for a in alerts],
                "count": len(alerts),
            }
        elif operation == "check_meetings":
            alerts = await self.check_upcoming_meetings(trace_id=trace_id)
            result_payload = {
                "alerts": [a.to_dict() for a in alerts],
                "count": len(alerts),
            }
        elif operation == "check_overdue":
            alerts = await self.check_overdue_tasks(trace_id=trace_id)
            result_payload = {
                "alerts": [a.to_dict() for a in alerts],
                "count": len(alerts),
            }
        elif operation == "is_deep_work":
            is_deep_work = await self.is_deep_work_time(trace_id=trace_id)
            result_payload = {
                "is_deep_work": is_deep_work,
                "deep_work_event": self._current_deep_work_event,
            }
        elif operation == "morning_briefing":
            briefing = await self.generate_morning_briefing(trace_id=trace_id)
            result_payload = briefing.to_dict()
        elif operation == "send_alerts":
            # Send provided alerts to all channels
            alerts_data = payload.get("alerts", [])
            alerts = [
                Alert(
                    alert_type=AlertType(a["alert_type"]),
                    priority=AlertPriority(a["priority"]),
                    message=a["message"],
                    entity_uuid=a.get("entity_uuid"),
                    entity_type=a.get("entity_type"),
                    due_at=a.get("due_at"),
                    metadata=a.get("metadata", {}),
                )
                for a in alerts_data
            ]
            send_result = await self.send_alerts_to_channels(
                alerts=alerts, trace_id=trace_id
            )
            result_payload = send_result.to_dict()
        else:
            result_payload = {"error": f"Unknown operation: {operation}"}

        return AgentMessage(
            source_agent=self.name,
            target_agent=msg.source_agent,
            intent="officer_result",
            payload=result_payload,
            trace_id=trace_id,
        )

    # =========================================================================
    # Main Operations
    # =========================================================================

    async def check_conditions(
        self,
        trace_id: str | None = None,
    ) -> AlertCheckResult:
        """
        Main monitoring routine - check all alert conditions.

        This is the primary entry point called by scheduler.

        Args:
            trace_id: Optional trace ID for logging.

        Returns:
            AlertCheckResult with all generated alerts.
        """
        start_time = time.time()
        all_alerts: list[Alert] = []

        logger.info(
            "[CHART] Officer checking conditions",
            extra={"trace_id": trace_id, "agent_name": self.name},
        )

        # Check if in quiet mode (Deep Work time)
        self._quiet_mode = await self.is_deep_work_time(trace_id=trace_id)

        if self._quiet_mode:
            logger.info(
                f"[WHISPER] Quiet mode active: {self._current_deep_work_event}",
                extra={"trace_id": trace_id, "agent_name": self.name},
            )

        # Run all checks
        deadline_alerts = await self.check_upcoming_deadlines(trace_id=trace_id)
        all_alerts.extend(deadline_alerts)

        meeting_alerts = await self.check_upcoming_meetings(trace_id=trace_id)
        all_alerts.extend(meeting_alerts)

        overdue_alerts = await self.check_overdue_tasks(trace_id=trace_id)
        all_alerts.extend(overdue_alerts)

        # Filter by quiet mode and debouncing
        alerts_to_send: list[Alert] = []
        for alert in all_alerts:
            if self._should_notify(alert):
                alerts_to_send.append(alert)
                self._mark_alert_sent(alert)

        # Limit alerts per check
        alerts_to_send = alerts_to_send[: self.officer_config.max_alerts_per_check]

        duration_ms = (time.time() - start_time) * 1000

        result = AlertCheckResult(
            alerts_found=len(all_alerts),
            alerts_sent=len(alerts_to_send),
            quiet_mode=self._quiet_mode,
            deep_work_event=self._current_deep_work_event,
            duration_ms=duration_ms,
            alerts=alerts_to_send,
        )

        logger.info(
            f"[BEACON] Officer check complete: "
            f"found {len(all_alerts)} alerts, sending {len(alerts_to_send)} "
            f"(quiet_mode={self._quiet_mode})",
            extra={"trace_id": trace_id, "agent_name": self.name},
        )

        return result

    # =========================================================================
    # Morning Briefing (#4)
    # =========================================================================

    async def generate_morning_briefing(
        self,
        trace_id: str | None = None,
    ) -> MorningBriefing:
        """
        Generate a proactive morning briefing with schedule, tasks, and alerts.

        The morning briefing includes:
        - Today's calendar events
        - High-priority tasks (urgent/high)
        - Overdue tasks
        - Any overnight alerts

        Args:
            trace_id: Optional trace ID for logging.

        Returns:
            MorningBriefing with all relevant information.
        """
        start_time = time.time()

        logger.info(
            "[CHART] Generating morning briefing",
            extra={"trace_id": trace_id, "agent_name": self.name},
        )

        # Get today's events
        events_today = await self._get_todays_events(trace_id=trace_id)

        # Get high-priority tasks
        high_priority_tasks = await self._get_high_priority_tasks(trace_id=trace_id)

        # Get overdue tasks
        overdue_tasks = await self._get_overdue_tasks_for_briefing(trace_id=trace_id)

        # Get overnight alerts (any alerts generated in last 12 hours)
        overnight_alerts = await self._get_overnight_alerts(trace_id=trace_id)

        # Generate greeting based on time and workload
        greeting = self._generate_greeting(
            event_count=len(events_today),
            task_count=len(high_priority_tasks),
            overdue_count=len(overdue_tasks),
        )

        # Generate summary text
        summary_text = self._generate_summary_text(
            events=events_today,
            high_priority=high_priority_tasks,
            overdue=overdue_tasks,
            alerts=overnight_alerts,
        )

        duration_ms = (time.time() - start_time) * 1000

        briefing = MorningBriefing(
            generated_at=datetime.now(),
            greeting=greeting,
            events_today=events_today,
            high_priority_tasks=high_priority_tasks,
            overdue_tasks=overdue_tasks,
            overnight_alerts=overnight_alerts,
            summary_text=summary_text,
            duration_ms=duration_ms,
        )

        logger.info(
            f"[BEACON] Morning briefing generated: "
            f"{len(events_today)} events, {len(high_priority_tasks)} high-priority tasks, "
            f"{len(overdue_tasks)} overdue",
            extra={"trace_id": trace_id, "agent_name": self.name},
        )

        return briefing

    async def _get_todays_events(
        self,
        trace_id: str | None = None,
    ) -> list[CalendarEventSummary]:
        """Get all calendar events for today."""
        # Calculate today's time bounds (midnight to midnight)
        now = datetime.now()
        today_start = datetime(now.year, now.month, now.day)
        today_end = today_start.replace(hour=23, minute=59, second=59)

        today_start_ms = int(today_start.timestamp() * 1000)
        today_end_ms = int(today_end.timestamp() * 1000)

        query = """
        MATCH (e:Event)
        WHERE e.start_time >= $start_ms
          AND e.start_time <= $end_ms
          AND e.expired_at IS NULL
        RETURN e.uuid as uuid, e.title as title, e.start_time as start_time,
               e.end_time as end_time, e.location_context as location,
               e.description as description
        ORDER BY e.start_time ASC
        LIMIT $limit
        """

        results = await self.neo4j.execute_query(
            query,
            {
                "start_ms": today_start_ms,
                "end_ms": today_end_ms,
                "limit": self.officer_config.default_query_limit,
            },
            trace_id=trace_id,
        )

        events = []
        for row in results:
            event = CalendarEventSummary(
                uuid=row.get("uuid", ""),
                title=row.get("title", "Unknown Event"),
                start_time=row.get("start_time", 0),
                end_time=row.get("end_time", 0),
                location=row.get("location"),
                description=row.get("description"),
            )
            events.append(event)

        return events

    async def _get_high_priority_tasks(
        self,
        trace_id: str | None = None,
    ) -> list[TaskSummary]:
        """Get high-priority tasks (urgent or high priority)."""
        query = """
        MATCH (t:Task)
        WHERE t.status IN ['todo', 'in_progress']
          AND t.priority IN ['urgent', 'high']
        OPTIONAL MATCH (t)-[:PART_OF]->(p:Project)
        RETURN t.uuid as uuid, t.action as action, t.priority as priority,
               t.status as status, t.due_date as due_date, p.name as project_name
        ORDER BY
            CASE t.priority WHEN 'urgent' THEN 0 ELSE 1 END,
            t.due_date ASC NULLS LAST
        LIMIT $limit
        """

        results = await self.neo4j.execute_query(
            query,
            {"limit": self.officer_config.default_query_limit},
            trace_id=trace_id,
        )

        tasks = []
        for row in results:
            task = TaskSummary(
                uuid=row.get("uuid", ""),
                action=row.get("action", "Unknown Task"),
                priority=row.get("priority", "medium"),
                status=row.get("status", "todo"),
                due_date=row.get("due_date"),
                project_name=row.get("project_name"),
            )
            tasks.append(task)

        return tasks

    async def _get_overdue_tasks_for_briefing(
        self,
        trace_id: str | None = None,
    ) -> list[TaskSummary]:
        """Get overdue tasks for the briefing."""
        query = """
        MATCH (t:Task)
        WHERE t.status IN ['todo', 'in_progress']
          AND t.due_date IS NOT NULL
          AND t.due_date < timestamp()
        OPTIONAL MATCH (t)-[:PART_OF]->(p:Project)
        RETURN t.uuid as uuid, t.action as action, t.priority as priority,
               t.status as status, t.due_date as due_date, p.name as project_name
        ORDER BY t.due_date ASC
        LIMIT $limit
        """

        results = await self.neo4j.execute_query(
            query,
            {"limit": self.officer_config.default_query_limit},
            trace_id=trace_id,
        )

        tasks = []
        for row in results:
            task = TaskSummary(
                uuid=row.get("uuid", ""),
                action=row.get("action", "Unknown Task"),
                priority=row.get("priority", "medium"),
                status=row.get("status", "todo"),
                due_date=row.get("due_date"),
                project_name=row.get("project_name"),
            )
            tasks.append(task)

        return tasks

    async def _get_overnight_alerts(
        self,
        trace_id: str | None = None,
    ) -> list[Alert]:
        """
        Get alerts that would have been generated overnight.

        This re-runs the alert checks to find any conditions
        that accumulated overnight.
        """
        # Get deadline warnings and overdue tasks
        deadline_alerts = await self.check_upcoming_deadlines(trace_id=trace_id)
        overdue_alerts = await self.check_overdue_tasks(trace_id=trace_id)

        # Combine and limit
        all_alerts = deadline_alerts + overdue_alerts
        return all_alerts[: self.officer_config.max_alerts_per_check]

    def _generate_greeting(
        self,
        event_count: int,
        task_count: int,
        overdue_count: int,
    ) -> str:
        """Generate a nautical greeting based on the day's outlook."""
        now = datetime.now()
        hour = now.hour

        # Time-based greeting
        if hour < 12:
            time_greeting = "Good morning, Captain"
        elif hour < 17:
            time_greeting = "Good afternoon, Captain"
        else:
            time_greeting = "Good evening, Captain"

        # Workload assessment
        total_items = event_count + task_count
        if overdue_count > 0:
            outlook = "There are overdue items requiring attention."
        elif total_items == 0:
            outlook = "Clear skies ahead - no events or urgent tasks today."
        elif total_items <= 3:
            outlook = "Calm seas - a manageable day ahead."
        elif total_items <= 6:
            outlook = "Moderate winds - a full but manageable schedule."
        else:
            outlook = "Choppy waters - a busy day ahead. Steady as she goes."

        return f"{time_greeting}. {outlook}"

    def _generate_summary_text(
        self,
        events: list[CalendarEventSummary],
        high_priority: list[TaskSummary],
        overdue: list[TaskSummary],
        alerts: list[Alert],
    ) -> str:
        """Generate a human-readable summary of the morning briefing."""
        lines = []

        # Events summary
        if events:
            lines.append(f"**Schedule**: {len(events)} event(s) today")
            for event in events[:5]:  # Limit display
                start_dt = datetime.fromtimestamp(event.start_time / 1000)
                time_str = start_dt.strftime("%H:%M")
                location = f" at {event.location}" if event.location else ""
                lines.append(f"  - {time_str}: {event.title}{location}")
            if len(events) > 5:
                lines.append(f"  - ... and {len(events) - 5} more")
        else:
            lines.append("**Schedule**: No events today")

        lines.append("")

        # High-priority tasks
        if high_priority:
            lines.append(f"**High Priority Tasks**: {len(high_priority)} task(s)")
            for task in high_priority[:5]:
                priority_marker = "[!]" if task.priority == "urgent" else "[H]"
                project = f" ({task.project_name})" if task.project_name else ""
                lines.append(f"  - {priority_marker} {task.action}{project}")
            if len(high_priority) > 5:
                lines.append(f"  - ... and {len(high_priority) - 5} more")
        else:
            lines.append("**High Priority Tasks**: None")

        lines.append("")

        # Overdue tasks
        if overdue:
            lines.append(f"**Overdue**: {len(overdue)} task(s) past due")
            for task in overdue[:3]:
                due_dt = datetime.fromtimestamp(task.due_date / 1000) if task.due_date else None
                due_str = f" (due {due_dt.strftime('%Y-%m-%d')})" if due_dt else ""
                lines.append(f"  - {task.action}{due_str}")
            if len(overdue) > 3:
                lines.append(f"  - ... and {len(overdue) - 3} more")
        else:
            lines.append("**Overdue**: All clear")

        # Alerts
        if alerts:
            lines.append("")
            lines.append(f"**Alerts**: {len(alerts)} item(s) need attention")

        return "\n".join(lines)

    def is_briefing_time(self) -> bool:
        """
        Check if current time matches the configured briefing time.

        Returns:
            True if it's briefing time, False otherwise.
        """
        now = datetime.now()
        return (
            now.hour == self.officer_config.briefing_hour
            and now.minute == self.officer_config.briefing_minute
        )

    # =========================================================================
    # Deep Work Detection
    # =========================================================================

    async def is_deep_work_time(
        self,
        trace_id: str | None = None,
    ) -> bool:
        """
        Check if the Captain is currently in a Deep Work calendar block.

        Deep Work events are detected by title keywords like "Deep Work",
        "Focus", or "Do Not Disturb".

        Args:
            trace_id: Optional trace ID for logging.

        Returns:
            True if in deep work mode, False otherwise.
        """
        keywords = self.officer_config.deep_work_keywords
        # Build regex pattern for keywords
        keyword_pattern = "|".join(keywords)

        query = """
        MATCH (e:Event)
        WHERE e.start_time <= timestamp()
          AND e.end_time >= timestamp()
          AND e.expired_at IS NULL
          AND e.title =~ $pattern
        RETURN e.uuid as uuid, e.title as title
        LIMIT 1
        """

        result = await self.neo4j.execute_query(
            query,
            {"pattern": f"(?i).*({keyword_pattern}).*"},
            trace_id=trace_id,
        )

        if result:
            self._current_deep_work_event = result[0].get("title")
            return True

        self._current_deep_work_event = None
        return False

    # =========================================================================
    # Deadline Alerts (#60)
    # =========================================================================

    async def check_upcoming_deadlines(
        self,
        hours: int | None = None,
        trace_id: str | None = None,
    ) -> list[Alert]:
        """
        Find tasks due within the specified time window.

        Generates WARNING alerts for high-priority tasks and INFO alerts
        for normal priority tasks.

        Args:
            hours: Hours until deadline (default from config).
            trace_id: Optional trace ID for logging.

        Returns:
            List of Alert objects for upcoming deadlines.
        """
        hours = hours or self.officer_config.deadline_warning_hours
        hours_ms = hours * 60 * 60 * 1000  # Convert to milliseconds

        logger.debug(
            f"[WHISPER] Checking deadlines within {hours} hours",
            extra={"trace_id": trace_id, "agent_name": self.name},
        )

        query = """
        MATCH (t:Task)
        WHERE t.status IN ['todo', 'in_progress']
          AND t.due_date IS NOT NULL
          AND t.due_date <= timestamp() + $hours_ms
          AND t.due_date > timestamp()
        OPTIONAL MATCH (t)-[:PART_OF]->(p:Project)
        RETURN t.uuid as uuid, t.action as action, t.due_date as due_date,
               t.priority as priority, t.status as status,
               p.name as project_name
        ORDER BY t.due_date ASC
        LIMIT $limit
        """

        results = await self.neo4j.execute_query(
            query,
            {
                "hours_ms": hours_ms,
                "limit": self.officer_config.default_query_limit,
            },
            trace_id=trace_id,
        )

        alerts: list[Alert] = []
        for row in results:
            priority = row.get("priority", "medium")
            alert_priority = (
                AlertPriority.WARNING if priority in ("urgent", "high") else AlertPriority.INFO
            )

            # Calculate hours remaining
            due_date = row.get("due_date", 0)
            now_ms = int(time.time() * 1000)
            hours_remaining = (due_date - now_ms) / (60 * 60 * 1000)

            message = f"Task due in {hours_remaining:.1f}h: {row.get('action', 'Unknown task')}"
            if row.get("project_name"):
                message += f" ({row['project_name']})"

            alert = Alert(
                alert_type=AlertType.DEADLINE_WARNING,
                priority=alert_priority,
                message=message,
                entity_uuid=row.get("uuid"),
                entity_type="Task",
                due_at=due_date,
                metadata={
                    "task_action": row.get("action"),
                    "task_priority": priority,
                    "task_status": row.get("status"),
                    "project_name": row.get("project_name"),
                    "hours_remaining": round(hours_remaining, 1),
                },
            )
            alerts.append(alert)

        logger.debug(
            f"[WHISPER] Found {len(alerts)} deadline warnings",
            extra={"trace_id": trace_id, "agent_name": self.name},
        )

        return alerts

