    # Alert Filtering and Debouncing
    # =========================================================================

    def _should_notify(self, alert: Alert) -> bool:
        """
        Determine if alert should be sent based on current conditions.

        Respects quiet mode (only CRITICAL alerts break silence) and
        debouncing (don't re-send same alert within debounce window).

        Args:
            alert: Alert to check.

        Returns:
            True if alert should be sent, False otherwise.
        """
        # In quiet mode, only CRITICAL alerts break silence
        if self._quiet_mode and alert.priority != AlertPriority.CRITICAL:
            return False

        # Check debouncing
        return not self._recently_sent(alert)

    def _recently_sent(self, alert: Alert) -> bool:
        """
        Check if a similar alert was recently sent.

        Uses alert type + entity UUID as the key for deduplication.

        Args:
            alert: Alert to check.

        Returns:
            True if alert was recently sent, False otherwise.
        """
        key = f"{alert.alert_type.value}:{alert.entity_uuid or 'none'}"
        last_sent = self._recent_alerts.get(key)

        if last_sent is None:
            return False

        debounce_seconds = self.officer_config.alert_debounce_minutes * 60
        elapsed = (datetime.now() - last_sent).total_seconds()

        return elapsed < debounce_seconds

    def _mark_alert_sent(self, alert: Alert) -> None:
        """
        Mark an alert as sent for debouncing.

        Args:
            alert: Alert that was sent.
        """
        key = f"{alert.alert_type.value}:{alert.entity_uuid or 'none'}"
        self._recent_alerts[key] = datetime.now()

    def clear_recent_alerts(self) -> None:
        """Clear the recent alerts cache (useful for testing)."""
        self._recent_alerts = {}

